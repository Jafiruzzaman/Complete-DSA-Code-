public class variable {
    public static void main(String[] args) {
      System.out.println("variables in java");
      int num = 10;
      System.out.println(num);
    }
}
/*
 * variables: variables are container to store a value
 * here:
 *  int : int is a datatype
 *  num : name of the variable
 *  10 is assign value
 */
/*
 * Rules while creating variable:
 * 1: variables name can't start with number or any special characters
 * 2: variables name can't be a keyword
 * 3: variables name can't contains space
 * 4: variables name must be start with characters, dollar sign($) and underscore(_)
 * 5: variables name should be meaningful
 */